def call_modal_endpoint(url: str, payload: dict) -> dict:
    """
    Helper function to call Modal endpoints with error handling.
    
    Args:
        url: Modal endpoint URL
        payload: Request payload
    
    Returns:
        Response JSON from Modal
    
    Raises:
        HTTPException if request fails
    """
    try:
        response = requests.post(
            url,
            json=payload,
            timeout=MODAL_TIMEOUT,
        )
        
        if response.status_code != 200:
            raise HTTPException(
                status_code=500,
                detail=f"Modal request failed: {response.status_code} - {response.text}"
            )
        
        return response.json()
    
    except requests.Timeout:
        raise HTTPException(
            status_code=504,
            detail="Modal request timed out. The model might be cold starting (first request takes longer)."
        )
    except requests.RequestException as e:
        raise HTTPException(
            status_code=500,
            detail=f"Failed to connect to Modal: {str(e)}"
        )



@router.get("/modal_status")
async def check_modal_status():
    """
    Check if Modal services are accessible.
    Useful for health checks and debugging.
    
    Returns status of all Modal endpoints.
    """
    status = {}
    
    # Check each service
    for service_name, url in MODAL_URLS.items():
        # Construct health check URL (assumes /health endpoint exists)
        health_url = url.replace("-endpoint.modal.run", "-health.modal.run")
        
        try:
            response = requests.get(health_url, timeout=5)
            if response.status_code == 200:
                status[service_name] = "healthy"
            else:
                status[service_name] = f"unhealthy (status: {response.status_code})"
        except requests.RequestException as e:
            status[service_name] = f"unreachable ({str(e)})"
    
    return {
        "modal_services": status,
        "all_healthy": all(s == "healthy" for s in status.values())
    }



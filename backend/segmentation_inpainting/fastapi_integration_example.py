@router.post("/qwen_white_to_scene", response_model=EditResponse)
async def qwen_white_to_scene_edit(request: QwenWhiteToSceneRequest):
    """
    Edit image using Qwen's white-to-scene LoRA.
    Converts white backgrounds into realistic scenes.
    
    Example usage:
    ```
    POST /editing/qwen_white_to_scene
    {
        "image_id": "abc123",
        "prompt": "modern office with plants and natural lighting",
        "num_inference_steps": 30
    }
    ```
    """
    try:
        # Get image and mask from storage
        image_base64 = get_image_base64(request.image_id)
        mask_base64 = get_mask_base64(request.image_id)
        
        # Prepare payload for Modal
        payload = {
            "image_base64": image_base64,
            "mask_base64": mask_base64,
            "prompt": request.prompt,
            "negative_prompt": request.negative_prompt,
            "num_inference_steps": request.num_inference_steps,
            "guidance_scale": request.guidance_scale,
            "strength": request.strength,
        }
        
        # Call Modal endpoint
        result = call_modal_endpoint(MODAL_URLS["qwen_white_to_scene"], payload)
        
        # Return response
        return EditResponse(
            success=result.get("success", True),
            image_base64=result["image_base64"],
            model_used="qwen",
            lora_used="white_to_scene",
            message="Successfully edited image with Qwen white-to-scene"
        )
    
    except HTTPException:
        raise
    except Exception as e:
        raise HTTPException(status_code=500, detail=f"Editing failed: {str(e)}")


